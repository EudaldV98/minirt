int		ft_strlen_nb(int nb)
{
	int		len;
	long	nbr;

	nbr = (nb < 0) ? -nb : nb;
	len = (nb == 0) ? 1 : 0;
	while (nb > 0)
	{
		len++;
		nb /= 10;
	}
	return (len);
}
